# Function to fit and print results
def fit_and_print(formula, df):
    model = smf.ols(formula=formula, data=df).fit()
    print("=== Model:", formula, "===")
    print(model.summary())
    print("\nR-squared: {:.3f}\n".format(model.rsquared))
    return model


